    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        try
        {
            if("".equals(p.getText()) || "".equals(m.getText()) || "".equals(ma.getText())||"".equals(s.getText())||"".equals(gst.getText()))
            {
                JOptionPane.showMessageDialog(rootPane,"All fields required!!!");
            }
            else
            {
                try
                {
                    Statement stmt=con.createStatement();
                    stmt.executeUpdate("insert into product(product_name,mrp,margin,stock,gst)values('"+p.getText()+"','"+m.getText()+"','"+ma.getText()+"','"+s.getText()+"','"+gst.getText()+"')");
                    JOptionPane.showMessageDialog(rootPane,"Product inserted success!!!");
                    selectPackage();
                     p.setText("");
                     m.setText("");
                      ma.setText("");
                      s.setText("");
                      gst.setText("");
                    stmt.close();
                }
                catch(Exception e)
                {
                     System.out.println(e);
                }
            }
            
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_addActionPerformed

