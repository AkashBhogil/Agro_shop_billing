      public void selectPackage()
    {
        try
        {
            
            product.removeAllItems();
            del_sel.removeAllItems();
            Statement stmt=con.createStatement();
            ResultSet res=stmt.executeQuery("select product_name from product");
            while(res.next())
            {
                product.addItem(res.getString(1));
                del_sel.addItem(res.getString(1));
            }
            stmt.close();
        }
        catch(Exception e)
        {
            
        }
              
    }
      
      void update()
      {
          try
          {
              String a=avail.getText();
              int a1=Integer.parseInt(a);
              String u=stock.getText();
              int u1=Integer.parseInt(u);
              int tot=a1+u1;
              Statement stmt=con.createStatement();
              stmt.executeUpdate("update product set stock='"+tot+"' where product_name='"+product.getSelectedItem().toString()+"'  ");
              stmt.close();
              JOptionPane.showMessageDialog(rootPane,"Updated !!");
              stock.setText("");
              selectPackage();
          }
          catch(Exception e)
          {
              JOptionPane.showMessageDialog(null,e);
          }
      }
